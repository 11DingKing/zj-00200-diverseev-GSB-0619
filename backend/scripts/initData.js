function initRegions() {
  console.log("\n开始初始化地域数据...");

  const regionStmt = db.prepare(`INSERT INTO regions 
    (name, code, terrain_type, climate, typical_usage, description)
    VALUES (?, ?, ?, ?, ?, ?)`);

  const reqStmt = db.prepare(`INSERT INTO region_vehicle_requirements 
    (region_id, category, min_range, max_range, min_weight, max_weight, min_price, max_price, terrain_suitability, priority, required_scenarios)
    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`);

  db.serialize(() => {
    db.run("BEGIN TRANSACTION");

    regions.forEach((region, index) => {
      regionStmt.run(
        region.name,
        region.code,
        region.terrain_type,
        region.climate,
        region.typical_usage,
        region.description,
        function (err) {
          if (err) {
            console.error(`插入地区 ${region.name} 失败:`, err.message);
            return;
          }
          const regionId = this.lastID;
          const reqs = regionRequirements[region.code] || [];
          reqs.forEach((req) => {
            reqStmt.run(
              regionId,
              req.category,
              req.min_range || null,
              req.max_range || null,
              req.min_weight || null,
              req.max_weight || null,
              req.min_price || null,
              req.max_price || null,
              region.terrain_type,
              req.priority || 0,
              req.required_scenarios
                ? JSON.stringify(req.required_scenarios)
                : null,
              (err) => {
                if (err) {
                  console.error(`插入地区需求失败:`, err.message);
                }
              },
            );
          });
        },
      );
      if ((index + 1) % 4 === 0) {
        console.log(`已插入 ${index + 1}/${regions.length} 个地区`);
      }
    });

    db.run("COMMIT", (err) => {
      if (err) {
        console.error("提交事务失败:", err.message);
      } else {
        console.log(`✓ 成功插入 ${regions.length} 个地区及需求数据`);
      }
      regionStmt.finalize();
      reqStmt.finalize();
    });
  });
}

function initAnnualDiversityStats() {
  console.log("\n开始生成年度多元化统计数据...");

  const years = [2020, 2021, 2022, 2023, 2024, 2025];
  const categoryMap = {
    微型代步: "micro",
    家用紧凑: "compact",
    中大型: "midsize",
    商用: "commercial",
  };

  const stmt = db.prepare(`INSERT OR REPLACE INTO annual_diversity_stats 
    (stat_year, total_vehicles, diversity_score, micro_count, compact_count, midsize_count, commercial_count,
     micro_ratio, compact_ratio, midsize_ratio, commercial_ratio, avg_weight, heavy_ratio, brand_count)
    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`);

  db.serialize(() => {
    db.run("BEGIN TRANSACTION");

    years.forEach((year, yearIdx) => {
      const yearProgress = (yearIdx + 1) / years.length;
      const yearVehicles = vehicles.filter(
        (v) =>
          (v.launch_year || vehicleLaunchYears[v.name] || 2023) <= year &&
          v.status !== "停售",
      );

      const total = yearVehicles.length;
      const categoryCounts = {
        micro: 0,
        compact: 0,
        midsize: 0,
        commercial: 0,
      };
      let totalWeight = 0;
      let heavyCount = 0;
      const brands = new Set();

      yearVehicles.forEach((v) => {
        const catKey = categoryMap[v.category];
        if (catKey) categoryCounts[catKey]++;
        totalWeight += v.curb_weight;
        if (v.curb_weight >= 1800) heavyCount++;
        brands.add(v.brand);
      });

      const baseRatios = {
        2020: { micro: 35, compact: 30, midsize: 20, commercial: 15 },
        2021: { micro: 32, compact: 33, midsize: 22, commercial: 13 },
        2022: { micro: 28, compact: 35, midsize: 25, commercial: 12 },
        2023: { micro: 24, compact: 36, midsize: 28, commercial: 12 },
        2024: { micro: 20, compact: 37, midsize: 30, commercial: 13 },
        2025: { micro: 18, compact: 38, midsize: 32, commercial: 12 },
      };

      const ratios = baseRatios[year] || baseRatios[2024];
      const scaledTotal = Math.round(20 + yearProgress * 140);
      const microCount = Math.round(scaledTotal * (ratios.micro / 100));
      const compactCount = Math.round(scaledTotal * (ratios.compact / 100));
      const midsizeCount = Math.round(scaledTotal * (ratios.midsize / 100));
      const commercialCount =
        scaledTotal - microCount - compactCount - midsizeCount;

      const simulatedTotal =
        microCount + compactCount + midsizeCount + commercialCount;
      const microRatio = (microCount / simulatedTotal) * 100;
      const compactRatio = (compactCount / simulatedTotal) * 100;
      const midsizeRatio = (midsizeCount / simulatedTotal) * 100;
      const commercialRatio = (commercialCount / simulatedTotal) * 100;

      const expectedPerCategory = simulatedTotal / 4;
      const maxDeviation =
        Math.max(
          Math.abs(microCount - expectedPerCategory) / expectedPerCategory,
          Math.abs(compactCount - expectedPerCategory) / expectedPerCategory,
          Math.abs(midsizeCount - expectedPerCategory) / expectedPerCategory,
          Math.abs(commercialCount - expectedPerCategory) / expectedPerCategory,
        ) * 100;

      const baseDiversity = 55 + yearProgress * 25;
      const diversityScore = Math.max(
        0,
        Math.min(100, baseDiversity - maxDeviation * 0.3),
      );

      const baseHeavyRatio = 30 + yearProgress * 15;
      const avgWeight = 1400 + yearProgress * 400;
      const brandCount = 8 + Math.round(yearProgress * 22);

      stmt.run(
        year,
        simulatedTotal,
        parseFloat(diversityScore.toFixed(1)),
        microCount,
        compactCount,
        midsizeCount,
        commercialCount,
        parseFloat(microRatio.toFixed(1)),
        parseFloat(compactRatio.toFixed(1)),
        parseFloat(midsizeRatio.toFixed(1)),
        parseFloat(commercialRatio.toFixed(1)),
        parseFloat(avgWeight.toFixed(1)),
        parseFloat(baseHeavyRatio.toFixed(1)),
        brandCount,
        (err) => {
          if (err) {
            console.error(`插入年度统计 ${year} 失败:`, err.message);
          }
        },
      );

      console.log(`已生成 ${year} 年度多元化统计数据`);
    });

    db.run("COMMIT", (err) => {
      if (err) {
        console.error("提交事务失败:", err.message);
      } else {
        console.log(`✓ 成功生成 ${years.length} 年年度多元化统计数据`);
      }
      stmt.finalize();
      console.log("\n数据初始化完成！");
      setTimeout(() => db.close(), 100);
    });
  });
}

