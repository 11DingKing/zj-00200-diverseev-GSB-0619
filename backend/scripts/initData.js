function initTierTrendStats() {
  console.log("\n开始生成分档趋势统计数据...");

  const weightTiers = [
    { name: "<1200kg", min: 0, max: 1200 },
    { name: "1200-1500kg", min: 1200, max: 1500 },
    { name: "1500-1800kg", min: 1500, max: 1800 },
    { name: "1800-2200kg", min: 1800, max: 2200 },
    { name: "2200-2800kg", min: 2200, max: 2800 },
    { name: ">=2800kg", min: 2800, max: Infinity },
  ];

  const months = [];
  const now = new Date();
  for (let i = 11; i >= 0; i--) {
    const d = new Date(now.getFullYear(), now.getMonth() - i, 1);
    months.push(d.toISOString().slice(0, 7));
  }

  const stmt = db.prepare(`INSERT INTO tier_trend_stats 
    (stat_date, tier_name, vehicle_count, indicator_type)
    VALUES (?, ?, ?, ?)`);

  db.serialize(() => {
    db.run("BEGIN TRANSACTION");

    months.forEach((month, monthIdx) => {
      const progress = (monthIdx + 1) / months.length;

      weightTiers.forEach((tier) => {
        const baseCount = vehicles.filter(
          (v) =>
            v.curb_weight >= tier.min &&
            v.curb_weight < tier.max &&
            v.status === "在售",
        ).length;

        const growthFactor = 0.4 + progress * 0.6;
        const randomVariation = 0.85 + Math.random() * 0.3;
        const count = Math.max(
          0,
          Math.round(baseCount * growthFactor * randomVariation),
        );

        stmt.run(month, tier.name, count, "weight", (err) => {
          if (err) {
            console.error(
              `插入分档趋势数据 ${month} ${tier.name} 失败:`,
              err.message,
            );
          }
        });
      });
    });

    db.run("COMMIT", (err) => {
      if (err) {
        console.error("提交事务失败:", err.message);
      } else {
        console.log(`✓ 成功生成分档趋势统计数据`);
      }
      stmt.finalize();
    });
  });
}

