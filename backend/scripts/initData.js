function initVehicles() {
  console.log("开始初始化车型数据...");

  const stmt = db.prepare(`INSERT INTO vehicles 
    (name, brand, category, curb_weight, range, price, scenarios, status, material, energy_density, launch_year)
    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`);

  db.serialize(() => {
    db.run("BEGIN TRANSACTION");

    vehicles.forEach((v, index) => {
      const launchYear = v.launch_year || vehicleLaunchYears[v.name] || 2023;
      stmt.run(
        v.name,
        v.brand,
        v.category,
        v.curb_weight,
        v.range,
        v.price,
        JSON.stringify(v.scenarios),
        v.status,
        v.material,
        v.energy_density,
        launchYear,
        function (err) {
          if (err) {
            console.error(`插入车型 ${v.name} 失败:`, err.message);
          }
        },
      );
      if ((index + 1) % 20 === 0) {
        console.log(`已插入 ${index + 1}/${vehicles.length} 条车型数据`);
      }
    });

    db.run("COMMIT", (err) => {
      if (err) {
        console.error("提交事务失败:", err.message);
      } else {
        console.log(`✓ 成功插入 ${vehicles.length} 条车型数据`);
      }
      stmt.finalize();
    });
  });
}

